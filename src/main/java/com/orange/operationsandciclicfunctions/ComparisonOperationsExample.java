package com.orange.operationsandciclicfunctions;

public class ComparisonOperationsExample {
    public static void main(String[] args) {
        int a= 3;
        int b= 1;
        System.out.println();
        System.out.println("For the variables a = "+a + " and b = "+b +" we have displayed the below comparison operations results:");
        boolean isEqual= a==b;
        System.out.println("Is equal:"+ isEqual);
        boolean isNotEqual = a!=b;
        System.out.println("Is not equal:"+isNotEqual);
        boolean isGreater= a>b;
        System.out.println("Is greater:"+isGreater);
        boolean isGreaterThan = a>=b;
        System.out.println("Is greater than:"+ isGreaterThan);
        boolean isLess= a<b;
        System.out.println("Is less:"+ isLess);
        boolean isLessThan = a<=b;
        System.out.println("Is less than:"+isLessThan);



    }
}
